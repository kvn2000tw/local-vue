    <p>敬請校長、主任、老師們給予指導，以下記錄城市文旅-博物館的故事「十八尖山 山林教育-山林茶趣」從個人到社區的永續生活實踐。 十八尖山是新竹人的森林公園，聊天散步、健走跑步，三三兩兩 好不熱鬧，各式亭臺充滿跳舞蹈、走拳法、吹樂器歡樂畫面，這是十八尖山日常。 然而市民有舒適的空間環境，卻對公園的歷史背景一知半解，也既熟悉又陌生，在日治時期1924年、1928年所發生什麼故事，相信有關全臺唯一的森林公園-十八尖山，一定很好奇吧！ 新竹市科學城社區大學由李校長世英帶領多元文化與全球視野教師群籌備一年時間，規劃十八尖山～山林教育系列活動，10月23日辦理場次「十八尖山 山林教育-山林茶趣」-夢龍擔任課程主持人，進行三小時的環境與文史導覽解說，並安排茶席展演及讓民眾體驗泡茶程序進行。 當日上午九點，三十位民眾準時報到，由吳主任以寧介紹活動簡介及介紹出席的本校各課程的講師，在致歡迎詞後，夢龍說明全程活動時間與實施地點和注意事項，讓每一位民眾配戴個人式導覽機，隨即進行登山步道導覽。 首先介紹新竹地區曾為道卡斯族的活動領域，竹塹社因狩獵追逐鹿群至十八尖山，清代漢人移墾定居竹塹，先人塚塋陸續安葬於此，1924年日據期間清塚，開闢森林環山路徑建遊覽公園，1928年正式啟用「全島唯一森林公園」，1929年民間募資向日本山口縣德山市訂製花崗岩造，訂製三十三座石觀音，禮請和尚開光祈福。 1945年日本離臺，國軍退防衝擊下，十八尖山成封閉之地。1963年開放後，1977年自由車場修建，民眾隨意擺設神像、搭蓋亭臺樓閣，都造成不同層次環境變遷或毀損。 回顧新竹市十八尖山在二十年前創舉辦理賞花月活動，每逢三月行銷新竹觀光產業，吸引全臺民眾關注花季期間的花卉博覽，現今雖無花團錦簇的場景，2024年也是正視<山林場域>為教育基地，讓民眾以森林浴場運動提升教育目標「山林療癒與木育教育、環境生態保護」。 沿著東峰步道前行，吸引眼球正是原設計花鐘的花台，兩側有裝設鐵門的山洞，十八尖山全區有十六座大小型態不一的防空戰備坑道口，東峰區域有五座。在此處有一個世界級研究機構的精密儀器-國家重力重力基準點站，從重力站觀看地球環境變遷、地震、火山、地下水、氣候等，測量分析並與全球各國資訊傳遞，分享地球科學研究。 繼續走在夾道二側桂花樹間，散發淡淡清香誘來鳳蝶飛舞，相思樹在頂上陽光，經枝葉間灑下光源，秋冬暖暖的舒服了身心，柏油路旁姑婆芋，大又厚的綠葉如傘有層次落差也是漂亮。夢龍再透過臺灣民間故事，簡單引述幾種植物的趣味性與真實性分享。 再前進九芎葛牆旁，看到第一座石觀音編號32號，引導民眾向前觀察，再提*卅二番*-卅字的發音、拼音，讓大家思考出正確答案。 再提從日治時期，新西國三十三觀音靈場，基隆、臺北、宜蘭、新竹等地設置概略緣由，告訴民眾可以尋找目前全臺灣，最多石觀音分佈最密集的十八尖山上，按二十四座番號、位置、法相等，製作石觀音秘笈，會是再到環境場域有學習及解謎的問題突破。 制高點有三座涼亭及奉茶設施的飲水機組，休息一下喝口水，再走到全臺灣最容易親近的小百岳三角點，拍攝一張留作紀念打卡照片上傳分享。三角點講解重點，是臺灣當時在日治時期為製作精細的地圖，運用三角點與點之間的高低、距離，由線連結網面有著精準的立體山體等高線相關地理資訊。 走在陵線上，往左看到唯一一座馬頭觀音法相，莊嚴公義的震撼感十足。另一座千手觀音也是目前山上最多尊的造型。叉路邊上也能再覓得一尊千手觀音法相。 繼續走二個彎道，多條步道再次匯成一個節點，就在人群聚集最熱鬧的觀音亭，夢龍先以圖卡示意，與民眾互動解說「臺灣茶」經發酵的程度，一般簡易區分，不發酵、20%-60%，甚至全發酵，六種茶的茶湯色澤的觀察。 茶席的佈置，擺設茶具，說明司茶及茶侶的關係，燙壺、淨杯、取茶葉、請茶友鑑茶型、泡茶、讀秒、倒茶的順序，邀請一起品茶。 開放體驗司茶流程，是環境教育非常重要的教案設計，真正在「境、淨、靜」身、心、零的五感體驗。 再次感謝本群組的國琴老師、兆祥老師、瑞福老師、雅婷老師、袁立老師，最疼惜夢龍給予支持的校長、主任，一年來經由視訊會議，一次次的討論及研究，梳理環境教育核心主軸，將理論分析編碼，再實境探勘磨合計畫架構，終於產品上架。經多次的教學活動學習，增進自己在教案的能量，呈現最佳十八尖山-山林教育行動教學教育成果。</p>
